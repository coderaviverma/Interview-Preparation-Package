package language.java.corebasic.inheritance;

public class A {

    A(){
        System.out.println("A constructor");
    }

    public void m1(){
        System.out.println("M1 A");
    }


    protected void m2(){
        System.out.println("M2 A");
    }

    void m3(){
        System.out.println("M3 A");
    }
    void m5(){
        System.out.println("M3 A");
    }

}
